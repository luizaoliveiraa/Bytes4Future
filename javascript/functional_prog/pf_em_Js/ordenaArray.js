// 33. Cria uma função que recebe um array como argumento e retorna um novo array com os elementos ordenados do maior ao menor.
// Utiliza o que aprendeste sobre o sort para resolver este exercício.

function ordenaDoMaiorAoMenor(array) {
  // escreve aqui a função
  return array.sort((a, b) => b - a);
}

