// perímetro de um retângulo, deve-se somar os seus quatro lados. Logo, o perímetro pode ser encontrado pela fórmula P=2(b+h).

function perimetroDoRectangulo(comprimento, largura) {
  return 2 * (comprimento + largura);
}

// Para calcular a área do retângulo, basta calcular o produto entre a sua base e a sua altura, ou seja, a área é dada pela fórmula A=b⋅h.
function areaDoRectangulo(comprimento, largura) {
  return comprimento * largura;
}
